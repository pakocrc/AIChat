import Foundation

enum ErrorMessages: String {
    case creatingRequest = "❌ Error: An error occurred creating the request."
    case executingRequest = "❌ Error: An error occurred while executing the request."
    case emptyResponse = "❌ Error: Empty response."
    case decodeFailed = "❌ Error: An error occurred while decoding."
}
